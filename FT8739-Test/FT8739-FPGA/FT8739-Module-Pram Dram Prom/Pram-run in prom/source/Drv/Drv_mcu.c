void DrvIOInit(void)
{
    POR_STA = 0x00;
    RST_STA = 0x66;

    P0SEL = 0xff;
    P1SEL = 0xff;
    P1MOD = 0x00;
    P0MOD = BITn(1) + BITn(0);

    P0DIR = 0xff;
    P1DIR = 0;//0xff;
}



